			<h2>How and when to water your plants during summer</h2>
			<p class="description">That is the eternal question and what worries us most when the heat arrives. Today we bring you some very easy tricks for watering plants in summer and on vacation.</p>
			<p>Keeping plants cool.The great solution for watering in summer (and all year round) are irrigation systems, but if not all our plants can have them, here are some tips that are sure to help you keep them fresh, green and beautiful during the hot months.</p>
			<p>Time to water!.Calm down, it is not necessary to set an alarm, but it is important to choose your watering time in summer. Avoid strong sunlight hours, because basically half of the water will evaporate.The best time is in the evening or early morning. Morning is the best time for the plant, because this is when the osmotic pressure is produced that makes the water circulate through stems, leaves and flowers, but part of this water is lost with the heat during the day. At night the soil has more time to absorb the water and will hold moisture longer.As both have advantages and disadvantages you can choose one of the two according to your schedule; this way you make sure you dont forget and spend a few minutes to enjoy your plants, either before leaving the house in the morning or when you return home in the evening.</p>
			<p>Watch out for the leaves!It is important not to leave the leaves with water, even if it is great to clean them from time to time to promote photosynthesis, especially in indoor plants, we do not want to leave drops or wet leaves.
                During the night, it can favor the appearance of mildew and if you water in the morning the sun can make a magnifying glass effect and burn them.
                To water your plants without wetting the leaves, plates will be your best allies. You can put the water directly in the dish and the plant will absorb it as it needs it. Remember that your CitySens modular shelves work great as plant saucers thanks to their one-centimeter raised rim.</p>
			<p>Water thoroughly!Almost more than increasing the frequency of watering, it is important to water better. We must make sure that the water soaks all the soil, so that all the roots receive their dose of water, which allows the plant to grow uniformly and absorb all the nutrients from the soil.</p>
			<p>Watch out! It is important to keep the humidity so that they do not rot in the heat, but remember that it is easier to recover a dry plant than a drowned one.</p>
			<p>In addition, the roots need a little dryness for the plant to grow. As in summer we need to keep them a little more humid you will see that they grow a little more slowly, for that reason the nutrients are going to become your great friends.</p>
